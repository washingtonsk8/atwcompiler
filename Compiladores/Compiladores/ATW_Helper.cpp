#include "ATW_Helper.h"
//---------------------------------------------------------------------------------------------------------------------
namespace ATW_HELPER{
	//---------------------------------------------------------------------------------------------------------------------
	const char* ATWgetCCStr(int _val){
		static char _conversion[255];
		_itoa_s(_val, _conversion, 10);
		string _return = _conversion;
		return _return.c_str();
	}
	//---------------------------------------------------------------------------------------------------------------------
	char* ATWgetCStr(int _val){
		static char _conversion[255];
		_itoa_s(_val, _conversion, 10);
		return _conversion;
	}
	//---------------------------------------------------------------------------------------------------------------------
}